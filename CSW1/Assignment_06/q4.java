public class q4 {
    public static void main(String[] args) {
        // Convert boolean to Boolean using autoboxing
        boolean booleanValue = true;
        Boolean booleanObject = booleanValue; // Autoboxing

        // Display the results
        System.out.println("Original boolean value: " + booleanValue);
        System.out.println("Converted Boolean object value: " + booleanObject);
    }
}
